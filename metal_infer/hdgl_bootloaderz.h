// hdgl_bootloaderz.h ? BootloaderZ V6.0 + HDGL-28 router integration header
#pragma once
#include <stdint.h>
#include <stddef.h>
#include <stdbool.h>
#include <stdlib.h>

// ?? APA state flags ??????????????????????????????????????????????????????????
#define APA_FLAG_SIGN_NEG  (1 << 0)
#define APA_FLAG_IS_NAN    (1 << 1)
#define APA_FLAG_GOI       (1 << 2)   // Gradual Overflow Infinity
#define APA_FLAG_GUZ       (1 << 3)   // Gradual Underflow Zero

// ?? System constants (match bootloaderZ.c) ???????????????????????????????????
#define BLZ_PHI            1.6180339887
#define BLZ_MAX_INSTANCES  8388608
#define BLZ_SLOTS_PER_INST 4
#define BLZ_CHUNK_SIZE     1048576     // 1 M slots per chunk
#define BLZ_MSB_MASK       (1ULL << 63)

// ?? MPI: arbitrarily-wide integer metadata ???????????????????????????????????
typedef struct {
    uint64_t *words;
    size_t    num_words;
    uint8_t   sign;        // 0 = positive, 1 = negative
} MPI;

// ?? Slot4096: Arbitrary Precision Architecture slot ??????????????????????????
typedef struct {
    uint64_t  *mantissa_words;
    MPI        num_words_mantissa;
    MPI        exponent_mpi;
    uint16_t   exponent_base;       // target base (4096)
    uint32_t   state_flags;
    MPI        source_of_infinity;
    size_t     num_words;
    int64_t    exponent;
    float      base;                // ?-scaled dynamic base
    int        bits_mant;
    int        bits_exp;
} Slot4096;

// ?? HDGLChunk / HDGLLattice ???????????????????????????????????????????????????
typedef struct {
    Slot4096 *slots;
    size_t    allocated;
} HDGLChunk;

typedef struct {
    HDGLChunk **chunks;
    int         num_chunks;
    int         num_instances;
    int         slots_per_instance;
    double      omega;
    double      time;
} HDGLLattice;

// ?? HDGL Router types ?????????????????????????????????????????????????????????
typedef struct {
    char *text;
    int   id;
} Token;

typedef struct {
    uint32_t last_feedback;
    int      last_expert_id;
} HDGL_History;

typedef struct {
    uint32_t charge;
    uint32_t entropy;
    uint32_t tension;
} HDGL_SlotState;

// ?? Shader dimension guard (passed as buffer(3) for non-square projections) ??
typedef struct {
    uint32_t in_dim;
    uint32_t out_dim;
} HDGL_ShaderDims;

// ?? APA utility API ??????????????????????????????????????????????????????????
Slot4096  slot_init_apa(int bits_mant, int bits_exp);
void      ap_free(Slot4096 *slot);
void      ap_copy(Slot4096 *dest, const Slot4096 *src);
void      ap_normalize(Slot4096 *slot);
void      ap_add(Slot4096 *A, const Slot4096 *B);
Slot4096 *ap_from_double(double value, int bits_mant, int bits_exp);
double    ap_to_double(const Slot4096 *slot);
void      ap_shift_right(uint64_t *mantissa_words, size_t num_words, int64_t shift_amount);

// ?? MPI utility API ??????????????????????????????????????????????????????????
void mpi_init(MPI *m, size_t initial_words);
void mpi_free(MPI *m);
void mpi_copy(MPI *dest, const MPI *src);
void mpi_set_value(MPI *m, uint64_t value, uint8_t sign);
// Alias used by hdgl_router.c (same as mpi_set_value for legacy exponent sync)
static inline void mpi_update_from_legacy(MPI *m, int64_t legacy_exp) {
    mpi_set_value(m, (uint64_t)llabs(legacy_exp), legacy_exp < 0 ? 1 : 0);
}

// ?? HDGL lattice API ?????????????????????????????????????????????????????????
HDGLLattice *lattice_init(int num_instances, int slots_per_instance);
HDGLChunk   *lattice_get_chunk(HDGLLattice *lat, int chunk_idx);
Slot4096    *lattice_get_slot(HDGLLattice *lat, int idx);
void         lattice_step_cpu(HDGLLattice *lat, double tick);
void         lattice_fold(HDGLLattice *lat);
void         lattice_free(HDGLLattice *lat);
double       prismatic_recursion(HDGLLattice *lat, int idx, double val);

// ?? Lattice-backed slot state (used by router) ????????????????????????????????
HDGL_SlotState *hdgl_get_slot_state(int lattice_id);

// ?? Bootloader integration ???????????????????????????????????????????????????
void init_apa_constants(void);
void free_apa_constants(void);
void bootloader_init_lattice(HDGLLattice *lat, int steps);
